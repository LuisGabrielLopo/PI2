package modelos.entidades;

/**
 *
 * @author luisg
 */
public class Usuarios {
    private String nome = "";
    private int cpf = 0;
    private String senha = "";

    public Usuarios() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
}
